def test_orphan_icon_fails(tmp_path: Path):
    icons = tmp_path / "icons"
    icons.mkdir()
    _write_svg(icons, "adc", "<svg/>")
    _write_svg(icons, "zzz-new", "<svg/>")  # not in order
    d2_out = tmp_path / "adi-components.d2"
    init_py = tmp_path / "__init__.py"
    init_py.write_text(_init_py("ADI_COMPONENTS"))

    result = embed_icons.generate_library(
        lib_name="adi",
        list_name="ADI_COMPONENTS",
        order=["adc"],
        icons_dir=icons,
        d2_output=d2_out,
        init_py=init_py,
        check=False,
    )
    assert not result.in_sync
    assert "unknown icons" in result.message
    assert "zzz-new" in result.message
    assert "_ORDER['adi']" in result.message


def test_generator_is_deterministic(tmp_path: Path):
    icons = tmp_path / "icons"
    icons.mkdir()
    _write_svg(icons, "adc", "<svg>X</svg>")
    _write_svg(icons, "dac", "<svg>Y</svg>")

    d2_a = tmp_path / "a.d2"
    d2_b = tmp_path / "b.d2"
    init_a = tmp_path / "init_a.py"
    init_b = tmp_path / "init_b.py"
    init_a.write_text(_init_py("ADI_COMPONENTS"))
    init_b.write_text(_init_py("ADI_COMPONENTS"))

    for d2_out, init_py in ((d2_a, init_a), (d2_b, init_b)):
        embed_icons.generate_library(
            lib_name="adi",
            list_name="ADI_COMPONENTS",
            order=["adc", "dac"],
            icons_dir=icons,
            d2_output=d2_out,
            init_py=init_py,
            check=False,
        )

    assert d2_a.read_text() == d2_b.read_text()
    assert init_a.read_text() == init_b.read_text()
